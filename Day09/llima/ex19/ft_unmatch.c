int		ft_unmatch(int *tab, int lenght)
{
	int con;
	int i;
	int j;

	con = 0;
	i = 0;
	j = 0;
	while (tab[i])
	{
		while (j < lenght)
		{
			if (tab[i] == tab[j])
			{
				con++;
			}
			j++;
		}
		if ((cont % 2) != 0)
		{
			return (tab[i]);
		}
		i++;
		j = 0;
	}
	return (0);
}
